    private void populateCombo()  {
        
        //Pour ajout on commence par récupérer le model existant de la liste
        DefaultComboBoxModel model = (DefaultComboBoxModel)fruitComboBox.getModel();
        model.addElement("Fruit de la passion");
        model.addElement ("Kiwi");
    
        //remplacer des données d'une combo
        DefaultComboBoxModel model2 = new DefaultComboBoxModel();
        model2.addElement("navet");
        model2.addElement("pomme de terre");
        model2.addElement("topinambour");
    
        //Affecter le nouveau model à la Combo
        fruitComboBox.setModel(model2);
        
             
    }
    
       private void populateYearCombo() {
           //Récupérer l'annéee en cours
         
        int currentYear = Calendar.getInstance().get(Calendar.YEAR);
        
        // Création d'un model pour la combo
        DefaultComboBoxModel model = new DefaultComboBoxModel();
        
        for (int i = 0; i < 10; i++) {
            model.addElement(currentYear +i);
        }
        jComboBox1.setModel (model);
       }
            
