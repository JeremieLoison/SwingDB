    private void calculerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculerButtonActionPerformed
//Récupérer le montant hors taxe saisi
    double coutHT = 1.2* Double.valueOf(montantHT.getText());
    double result;
    double taux;
    
    if(tva20RadioButton.isSelected()){
        taux = 1.2;
    } else if (jRadioButton15.isSelected()){
        taux = 1.155;
    } else{
        taux = 1.7;
        jRadioButton7.setSelected (true);
    }
    result = taux * coutHT;
    resultat.setValue(result);
   
    montantHT.requestFocus();
    }//GEN-LAST:event_calculerButtonActionPerformed

